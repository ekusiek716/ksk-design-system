#!/usr/bin/env node
/**
 * src/native/COMPONENT_LOOKUP.md generator.
 *
 * Usage:
 *   node scripts/generate-native-component-lookup.mjs
 *   node scripts/generate-native-component-lookup.mjs --check
 */
import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(__dirname, "..")
const INDEX = path.join(ROOT, "src/native/components/index.ts")
const OUTPUT = path.join(ROOT, "src/native/COMPONENT_LOOKUP.md")
const CHECK = process.argv.includes("--check")

function valueExportsFromBlock(block) {
  const body = block.slice(block.indexOf("{") + 1, block.lastIndexOf("}"))
  return body
    .split(",")
    .map((part) => part.trim())
    .filter(Boolean)
    .filter((part) => !part.startsWith("type "))
    .map((part) => part.split(/\s+as\s+/).pop().trim())
    .filter((name) => /^[A-Z]/.test(name))
}

function parseNativeExports() {
  const lines = fs.readFileSync(INDEX, "utf8").split(/\r?\n/)
  const rows = []
  let section = "Base"

  for (let i = 0; i < lines.length; i += 1) {
    const line = lines[i]
    const sectionMatch = line.match(/^\/\/\s*(Base|Phase\s+\d+:[^\n]+)/)
    if (sectionMatch) section = sectionMatch[1]

    if (!line.trimStart().startsWith("export {")) continue

    let block = line
    while (!/}\s+from\s+["']/.test(block) && i + 1 < lines.length) {
      i += 1
      block += "\n" + lines[i]
    }

    const sourceMatch = block.match(/from\s+["']([^"']+)["']/)
    if (!sourceMatch) continue
    const source = sourceMatch[1]

    for (const name of valueExportsFromBlock(block)) {
      rows.push({
        name,
        section,
        source: `src/native/components/${source.replace(/^\.\//, "")}.tsx`,
      })
    }
  }

  return rows
}

function render(rows) {
  const sorted = [...rows].sort((a, b) => {
    const sectionCmp = a.section.localeCompare(b.section, "en")
    return sectionCmp || a.name.localeCompare(b.name, "en")
  })

  return `# Native Component Lookup

<!-- This file is auto-generated by scripts/generate-native-component-lookup.mjs. Do not edit manually. -->

React Native / Expo consumer は、新規 UI を作る前にこの一覧で既存コンポーネントを確認してください。すべて \`ksk-design-system/native/ui\` から import します。

- Public native component exports: ${sorted.length}
- Import: \`import { Button, Screen } from "ksk-design-system/native/ui"\`

| Component | Source | Section |
|---|---|---|
${sorted.map((row) => `| \`${row.name}\` | \`${row.source}\` | ${row.section} |`).join("\n")}
`
}

const next = render(parseNativeExports())

if (CHECK) {
  const current = fs.existsSync(OUTPUT) ? fs.readFileSync(OUTPUT, "utf8") : ""
  if (current !== next) {
    console.error("[native-lookup] src/native/COMPONENT_LOOKUP.md is out of date.")
    console.error("Run: npm run generate:native-lookup")
    process.exit(1)
  }
  console.log("[native-lookup] OK")
} else {
  fs.writeFileSync(OUTPUT, next)
  console.log(`[native-lookup] wrote ${path.relative(ROOT, OUTPUT)}`)
}
